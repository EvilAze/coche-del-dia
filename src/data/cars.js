export function getCarOfDay() {
  // 1. Fecha de inicio del juego
  const start = new Date(2024, 0, 1);
  start.setHours(0, 0, 0, 0);

  // 2. Conseguimos la hora actual forzada a España (Península)
  const spainTime = new Date().toLocaleString("en-US", { timeZone: "Europe/Madrid" });
  const today = new Date(spainTime);
  today.setHours(0, 0, 0, 0);

  // 3. Calculamos la diferencia en días
  const diff = Math.floor((today - start) / (1000 * 60 * 60 * 24));
  
  return CARS[diff % CARS.length];
}
